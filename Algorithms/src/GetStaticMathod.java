/*
    得到静态方法
 */
public class GetStaticMathod {

    //get integer absolute
    public static int abs(int x){
        if( x < 0){
            x = -x;
        }
        return x;
    }

    //get double absolute
    public static double ads(int x){
        if(x < 0.0){
            x = -x;
        }
        return x;
    }

    // 

}
